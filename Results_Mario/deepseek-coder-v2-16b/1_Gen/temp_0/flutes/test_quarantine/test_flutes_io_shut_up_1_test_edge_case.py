
import sys
import os
from contextlib import contextmanager

@contextmanager
def shut_up(stderr: bool = True, stdout: bool = False):
    """
    Suppresses output to either :py:data:`~sys.stderr` or :py:data:`~sys.stdout`. This function can be used as a decorator or context manager.

    Parameters:
        stderr (bool): If ``True``, suppresses output from :py:data:`~sys.stderr`. Defaults to ``True``.
        stdout (bool): If ``True``, suppresses output from :py:data:`~sys.stdout`. Defaults to ``False``.

    Examples:
        Using the function as a decorator:
        ```python
        @shut_up(stderr=True)
        def verbose_func(...):
            ...
        ```

        Using the function as a context manager:
        ```python
        with shut_up(stderr=True):
            ... # verbose stuff
        ```
    """
    r""" Suppress output (probably generated by external script or badly-written libraries) for :py:data:`~sys.stderr` or :py:data:`~sys.stdout`. This method can be used as a decorator, or a context manager:

    .. code:: python

        @shut_up(stderr=True)
        def verbose_func(...):
            ...

        with shut_up(stderr=True):
            ... # verbose stuff

    :param stderr: If ``True``, suppress output from :py:data:`~sys.stderr`. Defaults to ``True``.
    :param stdout: If ``True``, suppress output from :py:data:`~sys.stdout`. Defaults to ``False``.
    """
    # redirect output to /dev/null
    fds = ([sys.stdout.fileno()] if stdout else []) + ([sys.stderr.fileno()] if stderr else [])
    null_fds = [os.open(os.devnull, os.O_RDWR) for _ in fds]
    output_fds = [os.dup(fd) for fd in fds]
    for null_fd, fd in zip(null_fds, fds):
        os.dup2(null_fd, fd)
    try:
        yield
    finally:
        # restore normal stderr
        for null_fd, output_fd, fd in zip(null_fds, output_fds, fds):
            os.dup2(output_fd, fd)
            os.close(null_fd)

