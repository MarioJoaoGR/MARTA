
import pytest
from sty import register

@pytest.fixture(scope="module")
def bg_register():
    return register.BgRegister()

def test_valid_inputs(bg_register):
    # Check that each color attribute is correctly initialized and accessible
    assert hasattr(bg_register, 'black')
    assert hasattr(bg_register, 'red')
    assert hasattr(bg_register, 'green')
    assert hasattr(bg_register, 'yellow')
    assert hasattr(bg_register, 'blue')
    assert hasattr(bg_register, 'magenta')
    assert hasattr(bg_register, 'cyan')
    assert hasattr(bg_register, 'li_grey')
    assert hasattr(bg_register, 'rs')
    assert hasattr(bg_register, 'da_grey')
    assert hasattr(bg_register, 'li_red')
    assert hasattr(bg_register, 'li_green')
    assert hasattr(bg_register, 'li_yellow')
    assert hasattr(bg_register, 'li_blue')
    assert hasattr(bg_register, 'li_magenta')
    assert hasattr(bg_register, 'li_cyan')
    assert hasattr(bg_register, 'white')
    assert hasattr(bg_register, 'da_black')
    assert hasattr(bg_register, 'da_red')
    assert hasattr(bg_register, 'da_green')
    assert hasattr(bg_register, 'da_yellow')
    assert hasattr(bg_register, 'da_blue')
    assert hasattr(bg_register, 'da_magenta')
    assert hasattr(bg_register, 'da_cyan')
    assert hasattr(bg_register, 'grey')

    # Check that the styles are instances of Style class
    assert isinstance(bg_register.black, register.Style)
    assert isinstance(bg_register.red, register.Style)
    assert isinstance(bg_register.green, register.Style)
    assert isinstance(bg_register.yellow, register.Style)
    assert isinstance(bg_register.blue, register.Style)
    assert isinstance(bg_register.magenta, register.Style)
    assert isinstance(bg_register.cyan, register.Style)
    assert isinstance(bg_register.li_grey, register.Style)
    assert isinstance(bg_register.rs, register.Style)
    assert isinstance(bg_register.da_grey, register.Style)
    assert isinstance(bg_register.li_red, register.Style)
    assert isinstance(bg_register.li_green, register.Style)
    assert isinstance(bg_register.li_yellow, register.Style)
    assert isinstance(bg_register.li_blue, register.Style)
    assert isinstance(bg_register.li_magenta, register.Style)
    assert isinstance(bg_register.li_cyan, register.Style)
    assert isinstance(bg_register.white, register.Style)
    assert isinstance(bg_register.da_black, register.Style)
    assert isinstance(bg_register.da_red, register.Style)
    assert isinstance(bg_register.da_green, register.Style)
    assert isinstance(bg_register.da_yellow, register.Style)
    assert isinstance(bg_register.da_blue, register.Style)
    assert isinstance(bg_register.da_magenta, register.Style)
    assert isinstance(bg_register.da_cyan, register.Style)
    assert isinstance(bg_register.grey, register.Style)
