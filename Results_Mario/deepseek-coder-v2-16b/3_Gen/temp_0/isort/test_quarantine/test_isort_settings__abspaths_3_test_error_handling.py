
import pytest
import os
from typing import Iterable

def _abspaths(cwd: str, values: Iterable[str]) -> set[str]:
    """
    Generate a set of absolute file paths based on the provided current working directory (cwd) and list of relative or absolute path strings.
    
    Parameters:
        cwd (str): The current working directory as a string. This is the base directory from which relative paths will be joined.
        values (Iterable[str]): An iterable containing strings representing file paths. These can be either relative paths starting with a separator or absolute paths ending without a separator.
    
    Returns:
        set[str]: A set of absolute file paths generated by joining the cwd with each value in `values`. If a value is an absolute path, it remains unchanged. If a value is a relative path that ends with a separator, it will be joined to the cwd. Otherwise, the original value is returned as-is.
    
    Examples:
        >>> _abspaths("home/user", ["folder1/", "file1.txt"])
        {'home/user/folder1/', 'file1.txt'}
        
        >>> _abspaths("/root", ["subdir/file2.txt", "/absolute/path"])
        {'/root/subdir/file2.txt', '/absolute/path'}
        
    Notes:
        - The function assumes that the provided `cwd` is an absolute path and does not check for its validity or existence within the system.
        - If a value in `values` starts with a separator but ends without one, it will be treated as a relative path and joined to `cwd`.
    """
    paths = {
        (
            os.path.join(cwd, value)
            if not value.startswith(os.path.sep) and value.endswith(os.path.sep)
            else value
        )
        for value in values
    }
    return paths

