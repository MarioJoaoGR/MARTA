def test_shut_up_with_stderr(capture_stdout, capture_stderr):
    @shut_up(stderr=True)
    def verbose_func():
        print("This should not be printed")
        sys.stderr.write("Error: This should be suppressed\n")
    
    verbose_func()
    assert capture_stderr().getvalue() == ""
    assert capture_stdout().getvalue() != ""  # Ensure stdout is unaffected

def test_shut_up_with_stdout(capture_stdout, capture_stderr):
    @shut_up(stdout=True)
    def verbose_func():
        print("This should be printed")
        sys.stderr.write("Error: This should not be suppressed\n")
    
    verbose_func()
    assert capture_stdout().getvalue() == "This should be printed"
    assert capture_stderr().getvalue() != ""  # Ensure stderr is unaffected

def test_shut_up_with_both(capture_stdout, capture_stderr):
    @shut_up(stderr=True, stdout=True)
    def verbose_func():
        print("This should not be printed")
        sys.stderr.write("Error: This should be suppressed\n")
        print("This should also not be printed")
    
    verbose_func()
    assert capture_stdout().getvalue() == ""
    assert capture_stderr().getvalue() == "Error: This should be suppressed\n"

