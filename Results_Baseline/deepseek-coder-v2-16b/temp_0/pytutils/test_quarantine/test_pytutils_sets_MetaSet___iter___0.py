
# Module: pytutils.sets
import pytest
from pytutils import MetaSet
import random
import attr

# Test initialization without initial values
def test_init_without_initial():
    meta_set = MetaSet()
    assert hasattr(meta_set, '_store'), "MetaSet should have a _store attribute"
    assert hasattr(meta_set, '_meta'), "MetaSet should have a _meta attribute"
    assert isinstance(meta_set._store, set), "_store should be an instance of set"
    assert isinstance(meta_set._meta, dict), "_meta should be an instance of dict"
    assert meta_set._initial is None, "_initial should default to None"

# Test adding multiple values to an empty set
def test_add_multiple_values():
    meta_set = MetaSet()
    values = [1, 2, 3]
    meta_set.update(values)
    assert len(meta_set._store) == 3, "Expected the _store to contain all added values"
    for value in values:
        assert value in meta_set._store, f"Value {value} should be in the set"

# Test checking if a value is in the set after adding multiple values
def test_contains():
    meta_set = MetaSet()
    values = [1, 2, 3]
    meta_set.update(values)
    assert 1 in meta_set, "Expected 1 to be in the set"
    assert 4 not in meta_set, "Expected 4 not to be in the set"

# Test iterating over the set and printing each value's metadata
def test_iteration():
    meta_set = MetaSet()
    values = [1, 2, 3]
    meta_set.update(values)
    for value in meta_set:
        assert value in meta_set._store, f"Value {value} should be in the set"
        assert isinstance(meta_set._meta[value], int), "Metadata should be an integer"

# Test removing a value from the set
def test_remove():
    meta_set = MetaSet()
    values = [1, 2, 3]
    meta_set.update(values)
    meta_set.discard(2)
    assert 2 not in meta_set, "Expected 2 to be removed from the set"

# Test customizing the _meta_func to generate different unique identifiers
def test_custom_meta_func():
    def custom_meta_func(value):
        return value * 10

    meta_set = MetaSet(_meta_func=custom_meta_func)
    values = [1, 2, 3]
    meta_set.update(values)
    for value in meta_set:
        assert isinstance(meta_set._meta[value], int), "Metadata should be an integer generated by custom function"
        assert meta_set._meta[value] == value * 10, "Expected metadata to be the result of custom function"

