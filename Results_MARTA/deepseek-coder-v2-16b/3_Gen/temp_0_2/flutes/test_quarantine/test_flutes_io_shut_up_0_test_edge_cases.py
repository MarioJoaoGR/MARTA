
import sys
import os
import io
import pytest
from contextlib import redirect_stdout, redirect_stderr

def shut_up(stderr: bool = True, stdout: bool = False):
    r""" Suppress output (probably generated by external script or badly-written libraries) for :py:data:`~sys.stderr` or :py:data:`~sys.stdout`. This function can be used as a decorator, or context manager.

    Examples:
        Using the function as a decorator:
        ```python
        @shut_up(stderr=True)
        def verbose_func(...):
            ...
        ```

        Using the function as a context manager:
        ```python
        with shut_up(stderr=True):
            ... # verbose stuff
        ```

    :param stderr: If ``True``, suppress output from :py:data:`~sys.stderr`. Defaults to ``True``.
    :param stdout: If ``True``, suppress output from :py:data:`~sys.stdout`. Defaults to ``False``.
    """
    # redirect output to /dev/null
    fds = ([sys.stdout.fileno()] if stdout else []) + ([sys.stderr.fileno()] if stderr else [])
    null_fds = [os.open(os.devnull, os.O_RDWR) for _ in fds]
    output_fds = [os.dup(fd) for fd in fds]
    for null_fd, fd in zip(null_fds, fds):
        os.dup2(null_fd, fd)
    yield
    # restore normal stderr
    for null_fd, output_fd, fd in zip(null_fds, output_fds, fds):
        os.dup2(output_fd, fd)
        os.close(null_fd)

@pytest.fixture
def capture_stdout():
    out = io.StringIO()
    with redirect_stdout(out):
        yield out

@pytest.fixture
def capture_stderr():
    err = io.StringIO()
    with redirect_stderr(err):
        yield err

def test_shut_up_decorator(capture_stdout, capture_stderr):
    @shut_up(stderr=True)
    def verbose_func():
        print("This should not be printed", file=sys.stdout)
        print("This should not be printed either", file=sys.stderr)
    
    verbose_func()
    assert capture_stdout.getvalue() == ""
    assert capture_stderr.getvalue() == ""

def test_shut_up_context_manager(capture_stdout, capture_stderr):
    with shut_up(stderr=True):
        print("This should not be printed", file=sys.stdout)
        print("This should not be printed either", file=sys.stderr)
    
    assert capture_stdout.getvalue() == ""
    assert capture_stderr.getvalue() == ""

def test_shut_up_none():
    @shut_up(stderr=None, stdout=None)
    def verbose_func():
        print("This should not be printed", file=sys.stdout)
        print("This should not be printed either", file=sys.stderr)
    
    with pytest.raises(TypeError):
        verbose_func()

def test_shut_up_empty_list():
    @shut_up(stderr=[], stdout=[])
    def verbose_func():
        print("This should not be printed", file=sys.stdout)
        print("This should not be printed either", file=sys.stderr)
    
    with pytest.raises(TypeError):
        verbose_func()

